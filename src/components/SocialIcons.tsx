interface SocialIconsProps {
    size?: 'sm' | 'md' | 'lg';
    variant?: 'default' | 'circle' | 'circle-outline';
    showLabels?: boolean;
    className?: string;
}

const SocialIcons = ({ size = 'md', variant = 'default', showLabels = false, className = '' }: SocialIconsProps) => {
    const iconSize = size === 'sm' ? 16 : size === 'md' ? 20 : 24;
    const circleSize = size === 'sm' ? 'w-10 h-10' : size === 'md' ? 'w-12 h-12' : 'w-14 h-14';

    const socialLinks = [
        { name: 'Apple Music', url: SOCIAL_LINKS.APPLE_MUSIC, icon: AppleMusicIcon },
        { name: 'Beatport', url: SOCIAL_LINKS.BEATPORT, icon: BeatportIcon },
        { name: 'Facebook', url: SOCIAL_LINKS.FACEBOOK, icon: FacebookIcon },
        { name: 'Instagram', url: SOCIAL_LINKS.INSTAGRAM, icon: InstagramIcon },
        { name: 'SoundCloud', url: SOCIAL_LINKS.SOUNDCLOUD, icon: SoundCloudIcon },
        { name: 'Spotify', url: SOCIAL_LINKS.SPOTIFY, icon: SpotifyIcon },
        { name: 'Twitter', url: SOCIAL_LINKS.TWITTER, icon: TwitterIcon },
        { name: 'YouTube', url: SOCIAL_LINKS.YOUTUBE, icon: YoutubeIcon },
    ];

    if (variant === 'circle' || variant === 'circle-outline') {
        return (
            <div className={`flex flex-wrap justify-center items-center gap-4 ${className}`}>
                {socialLinks.map((social) => (
                    <a
                        key={social.name}
                        href={social.url}
                        target="_blank"
                        rel="noopener noreferrer"
                        className={`${circleSize} flex items-center justify-center rounded-full transition-all duration-300 ${
                            variant === 'circle'
                                ? 'bg-white text-black hover:bg-[#D4AF37] hover:scale-110'
                                : 'border-2 border-white text-white hover:border-[#D4AF37] hover:text-[#D4AF37] hover:scale-110'
                        }`}
                        aria-label={social.name}
                        title={social.name}
                    >
                        <social.icon size={iconSize} />
                    </a>
                ))}
            </div>
        );
    }

    return (
        <div className={`flex flex-wrap items-center gap-4 ${className}`}>
            {socialLinks.map((social) => (
                <a
                    key={social.name}
                    href={social.url}
                    target="_blank"
                    rel="noopener noreferrer"
                    className="text-gray-400 hover:text-white transition-colors duration-300"
                    aria-label={social.name}
                    title={social.name}
                >
                    <social.icon size={iconSize} />
                    {showLabels && <span className="ml-2 text-sm">{social.name}</span>}
                </a>
            ))}
        </div>
    );
};

